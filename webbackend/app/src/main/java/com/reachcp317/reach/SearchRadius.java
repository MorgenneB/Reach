package com.reachcp317.reach;

/**
 * Object to hold the length of the Event search radius
 * @author Morgenne Besenschek
 *
 */
public class SearchRadius {
	private Integer radius;
	
	public SearchRadius(Integer radius) {
		this.radius = radius;
	}
	
	public Integer getRadius() {
		return this.radius;
	}
	
	public void setRadius(Integer radius) {
		this.radius = radius;
	}
	
}
